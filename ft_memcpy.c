#include "libft.h"
#include <stdio.h>
#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t i;

	if (!dst && !src)
		return (NULL);
	i = 0;
	while (i < n)
	{
		((unsigned char*)dst)[i] = ((unsigned char*)src)[i];
		i++;
	}
	return (dst);
}

int		main(void)
{
	char src[100] = "this is a test";
	char dst[5];

	ft_memcpy(dst, src, 25);
	printf("%s\n", dst);
}
